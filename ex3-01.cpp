    X=E*B*D-F%A;
    cout<<"C%F+A+B/E ="<<X<<endl;
    X=C%F+A+B/E;
    cout<<"A-B*E/B%D ="<<X<<endl;
    X=A-B*E/B%D;
    cout<<"F*A-D+E*D ="<<X<<endl;
    X=F*A-D+E*D;
